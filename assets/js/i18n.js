const DEFAULT_LANG = 'it';
const STORAGE_KEY = 'dw-lang';

function getLang() {
  return localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG;
}

function setLang(lang) {
  localStorage.setItem(STORAGE_KEY, lang);
}

function t(key, lang) {
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || (TRANSLATIONS[DEFAULT_LANG][key]) || key;
}

function applyTranslations(lang) {
  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key, lang);
    if (val) el.textContent = val;
  });

  // HTML content (for elements with <br> etc.)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    const val = t(key, lang);
    if (val) el.innerHTML = val;
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = t(key, lang);
    if (val) el.setAttribute('placeholder', val);
  });

  // Update active lang indicator
  document.querySelectorAll('[data-lang]').forEach(el => {
    el.classList.toggle('lang-active', el.getAttribute('data-lang') === lang);
  });
}

function initI18n() {
  const lang = getLang();

  // Wire up language switcher buttons
  document.querySelectorAll('[data-lang]').forEach(el => {
    el.addEventListener('click', e => {
      e.preventDefault();
      const newLang = el.getAttribute('data-lang');
      setLang(newLang);
      applyTranslations(newLang);
    });
  });

  applyTranslations(lang);
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}
